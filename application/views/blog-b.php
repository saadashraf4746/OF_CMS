				accepting a small down payment of between 5-10%, and even working out a favorable 
				post-handover plan that lets you rent out or live in the property first before paying 
				it all off. In some cases developers are also waiving the 4% registration fee and service 
				charges, even extending to 10 years, to further stimulate the off-plan market, and to reduce 
				the financial burden on those wanting to invest.
			</p>

