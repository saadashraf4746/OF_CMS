			<h3>Myth #2: It’s Risky</h3>
			<p>
				There’s a common misconception that buying a secondary property is more 
				affordable and easier to secure a mortgage when in actual fact, buying 
				off-plan can be cheaper, especially if you get in there early. Under-construction 
				properties are typically priced lower than ready properties, as essentially you 
				are buying into its potential rather than physical attributes, and carry a higher 
				probability of capital appreciation near to completion. Whereas the closer to handover 
				you get, the bigger the chance of the price increases, especially if the buzz is sustained 
				around the project and its surrounding areas. You’ve heard the phrase: ‘The early bird 
				catches the worm’ and that’s undoubtedly the case when it comes to off-plan.
			</p>
